    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        /**
         * Para quitar el apa単o del boton de acceder al menu clientes,
         * comentar las siguientes 3 lineas y descomentar el resto.
         */
        String pass_string = new String (fieldpass.getPassword());
                  
       if(fieldusuario.getText().isEmpty() || fieldpass.getPassword().length==0){
           JOptionPane.showMessageDialog(this,"Debes introducir un usuario y una contrase単a.","Alert",JOptionPane.WARNING_MESSAGE);        
       }
        else{
           
            if(controlador.comprobarExistenciaCliente(fieldusuario.getText(), pass_string )){
                Cliente cliente = new Cliente(controlador, controlador.devolverDni(fieldusuario.getText()));
                cliente.setVisible(true);
                this.dispose();
            }
            else{
                if(controlador.comprobarLoginGerente(fieldusuario.getText(), pass_string)){
                    MenuGerente mg = new MenuGerente(controlador);
                    mg.setVisible(true);
                    this.dispose();
                }else{          
                   JOptionPane.showMessageDialog(this,"Error: Usuario o contrase単a incorrectos","Alert",JOptionPane.WARNING_MESSAGE);
                }
            }               
       }
        
      
    
        
        
    }//GEN-LAST:event_entrarActionPerformed

