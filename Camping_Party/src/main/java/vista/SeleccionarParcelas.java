    private void anyadirParcelasAVista(){
        int tam = controlador.numeroDeParcelas();
        ArrayList<String> id = controlador.getIDs();
        for(int i=0; i<tam; i++)
        {
            if(!controlador.parcelaOcupada(id.get(i),entrada,salida))
            {
                comboParcelas.addItem(id.get(i));
                ocupade.add("Libre");
            }
        }
    }
        

    
    

